            <p>Fenomena kebakaran hutan dan lahan (karhutla) di Kalimantan Barat, agaknya
                sudah menjadi rutinitas tahunan. Karhutla hampir terjadi pada setiap musim
                kemarau, khususnya pada saat kemarau ekstrim akibat El-Nino. Kondisi ini
                diperparah dengan  kondisi krisis iklim yang menyebabkan perubahan cuaca
                ekstrem seperti gelombang panas dan kekeringan, belum lagi  faktor aktivitas
                manusia juga kerap menjadi penyebab masifnya kebakaran hutan di Kalimantan
                Barat.</p>
            <div>
                <img src="images/aksi-nyata1.png" class="" alt="">
            </div>
            <p>Data yang dilansir dari SiPongi+, yang merupakan fasilitas laman web guna
                memantau karhutla milik Kementerian Lingkungan Hidup dan Kehutanan
                (KLHK), menunjukan seluas 19.633 hektar lahan dan hutan terbakar di
                Kalimantan Barat sepanjang periode Agustus 2022. </p>
            <p>
                Rizqy Fathi, yang akrab disapa Iki, memaparkan bahwa dirinya tak bisa melihat
                dan bernafas dengan baik ketika karhutla sedang terjadi di Kubu Raya,
                Kalimantan Barat. Sebab, asap yang dihasilkan tersebut berasal dari lahan
                gambut, yang diketahui memiliki kandungan racun yang lebih tinggi dari pada
                asap pembakaran rumah tangga.
            </p>
            <p>
                “Dulu waktu kecil aku cukup senang jika sedang terjadi bencana karhutla,
                karena sekolah pasti diliburkan. Tapi, waktu aku beranjak dewasa aku mulai
                paham kalau ini masalah serius. Dulu ketika asap ada di mana-mana, aku gak
                bisa lihat karena jarak pandang terbatas, dan gak bisa nafas juga. Parah lah
                pokoknya” singkap Iki (27), 17 September lalu di Posko Pemadam Kapur Raya.
            </p>
            <h2 class="mb-4">Pemadam Kapur Raya </h2>
            <p>
                Berangkat dari pengalaman dan keresahan yang cukup pahit akibat dampak
                karhutla pada 2020–Iki, Kelvin, dan Aldy yang juga tergabung di Tim Cegah Api
                (TCA) membentuk unit pemadam kebakaran di Desa Kapur, Kubu Raya dengan
                bermodal alat seadanya.
            </p>
            <div class="mb-4">
                <img src="images/aksi-nyata2.png" alt="">
            </div>
            <p>Dampak karhutla tak memandang latar belakang, umur, dan profesi seseorang.
                Semua makhluk hidup akan terancam dengan berbagai dampak yang dirasakan,
                bahkan hingga berujung kematian atau kepunahan. Selain itu, tingginya angka
                karhutla sangat berbanding terbalik dengan ketersediaan fasilitas pemadam di
                Kabupaten Kubu Raya. Karena itu, meski serba terbatas, mereka merasa bahwa
                mereka harus bergerak untuk merespon bencana karhutla yang sering kali
                terjadi. </p>
            <p>
                “Dulu kami hanya bermodalkan motor dan mesin sederhana yang dibeli pakai
                duit hasil donasi teman-teman, Kak. Kami juga tidak belajar secara teori
                tentang safety dan tata cara memadamkan api. Langsung aja gitu, belajarnya di
                TKP. Walaupun hanya bermodal seperti itu, kami tetap jalan. Yang penting kami
                bergerak..” kenang Kelvin (20)
            </p>
            <p>
                “Motornya juga seadanya aja, Kak. Saat ini kami punya armada mobil pick up
                gitu, butut juga sebenarnya. Tapi kami maksimalin dan kami modif sedemikian
                rupa supaya bisa dan layak dipakai untuk beroperasi. Tapi Alhamdulillah.”
                Tambah Aldy (23)
            </p>
            <p>
                Hal tersebut menjadi salah satu alasan mengapa mereka bergabung dengan
                TCA. Menurut mereka, dengan bergabung dengan TCA mereka bisa
                mendapatkan ilmu tata cara memadamkan kebakaran dengan benar dan aman.
                Kemudian, nantinya ilmu tersebut akan turut diimplementasikan dan dibagi
                kepada anggota pemadam Kapur Raya lainnya.
            </p>
            <div class="mb-4"><img src="images/aksi-nyata3.png" alt=""></div>
            <p>
                Kini, unit pemadaman yang mereka buat sudah menginjak umur tiga tahun.
                Walaupun masih terbilang seumur jagung, terhitung mereka telah mempunyai
                20 anggota aktif yang rata-rata diisi oleh anak muda. Selain itu, mereka sudah
                mempunyai dua armada mobil dan dua armada motor yang biasa mereka
                gunakan untuk beroperasi. 
            </p>
            <p>
                Semua anggota Pemadam Kapur Raya benar-benar bergerak berdasarkan hati
                dan jiwa mereka. Bagaimana tidak, untuk pekerjaan yang terbilang berbahaya
                dan tak mengenal waktu, mereka rela dengan tidak menerima gaji demi
                melayani masyarakat dan merespon bencana karhutla yang acap kali terjadi di
                daerah mereka. 
            </p>
            <p>
                “Gak digaji, Kak. Boleh ditanya ke anggota lainnya. Jadi kita punya pekerjaan
                sampingan. Aku jadi freelance nyanyi di Kafe gitu. Kalau Kelvin jadi kurir
                pengantar barang, dan Iki pernah jadi admin di suatu klinik gitu. Di sini
                memang bukan cari duit, Kak. Untuk yang berjiwa sosial aja.” Ucap Aldy.
            </p>
            <div class="mb-4"><img src="images/aksi-nyata4.png" alt=""></div>
            <p>Selain pelayanan pemadaman kebakaran, mereka juga melakukan rescue atau
                penyelamatan terhadap hewan-hewan terdampak. Nantinya, mereka akan
                merawat hewan tersebut, lalu dikembalikan ke hutan seiring kondisinya
                terpantau membaik. </p>
            <p>
                “Seringnya, kami merescue ular. Ini salah satu pengalaman pahitku,
                menemukan ular sanca kembang besar sekali. Tapi pas didekati dan mau
                direscue ternyata ularnya sudah habis terbakar. Gosong semua dan mati.” Tegas
                Kelvin.
            </p>
            <h2 class="mb-4">Sulitnya memadamkan api lahan gambut</h2>
            <p>
                Aldy, Kelvin, dan Iki mengaku bahwa kebakaran lahan gambut lebih susah
                dipadamkan dari pada lahan mineral. Pada kondisi normal, lahan gambut
                sejatinya bersifat seperti spons yang menyerap dan menahan air. Namun, jika
                musim kemarau gambut bisa dengan mudah terbakar. Cukup mengherankan,
                alih-alih dijaga agar tidak terbakar, namun nyatanya lahan gambut sering kali
                sengaja dibakar dalam skala masif guna pembukaan lahan sawit. 
            </p>
            <div class="mb-4"><img src="images/aksi-nyata5.png" alt=""></div>
            <p>
                Walaupun api di permukaan sudah hilang, bukan berarti api benar-benar
                padam. Dikarenakan, kemungkinan besar di bawah tanah masih terdapat api
                yang menyala dan bahkan lebih besar dari pada api yang muncul di permukaan.
                Mengingat gambut merupakan akumulasi sisa-sisa tumbuhan setengah
                membusuk yang berada di dalam tanah—fosil tumbuhan-tumbuhan tersebut,
                ibarat menjadi bahan bakar yang bisa memantik api menjadi lebih besar. Hal ini
                juga menjadi penyebab mengapa asap dari pembakaran lahan gambut
                berwarna hitam dan tebal.
            </p>
            <p>
                “Aku sampai pernah pingsan waktu madamin api karhutla gambut di Ketapang,
                karena luasan kebakarannya cukup luas dan asapnya benar-benar tebal. Aku
                sampai tidak bisa melihat temanku dan mesin. Sampai akhirnya, tim
                memutuskan untuk tarik mundur. Kita cuma bisa berharap sama Tuhan. Ibarat
                cuma Tuhan dan hujan aja yang bisa madamin apinya.” Pungkas Iki
            </p>
            <h2 class="mb-4">Kami butuh solusi nyata</h2>
            <p>
                KLHK mengklaim bahwa luasan karhutla pada pada Januari hingga Juli 2022
                tercatat 87.703 hektar, atau mengalami penurunan 19,1% dibandingkan dengan
                periode sama pada 2021. Namun kita tentu belum boleh senang, karena angka
                tersebut masih tergolong cukup tinggi untuk luasan karhutla. Selain itu,
                penurunan ini disebabkan oleh anomali fenomena La Nina, dan bukan
                sepenuhnya hasil upaya pemerintah.
            </p>
            <p>
                Kemudian, ditambah krisis iklim yang sangat mengancam. Krisis iklim akan
                terus menjadi pola atau lingkaran hitam yang tak kunjung usai bila tak segera
                ditangani dengan cepat. Krisis iklim akan memperparah kondisi karhutla akibat
                kenaikan suhu, kekeringan, dan gelombang panas.  
            </p>
            <p>
                Kehadiran Undang-Undang Cipta Kerja yang sangat memaksa, eksklusif, dan
                prematur sangat berpotensi semakin melemahkan penegakan hukum terkait
                kasus karhutla di Indonesia. Hal ini menjadi ancaman besar karena terdapat
                banyak peraturan yang tidak proaktif terhadap lingkungan, khususnya terkait
                pembakaran hutan dan lahan. 
            </p>
            <p>
                Maka dari itu, perlu upaya serius dari pemerintah untuk segera menangani
                permasalahan ini. Janji atau ratifikasi perjanjian pada Persetujuan Paris tidaklah
                cukup. Kami perlu aksi dan solusi nyata terkait penanganan krisis iklim dan
                deforestasi–seperti menghentikan deforestasi dengan kebijakan yang tegas
                dan proaktif terhadap lingkungan, transisi energi ramah lingkungan, dan
                penghentian budaya represif dan diskriminatif negara terhadap masyarakat
                adat yang menjaga hutannya. 
            </p>
            <h2 class="mb-4">Harapan terdalam</h2>
            <p>
                Bagaimanapun, seharusnya pemerintah berterima kasih dan berkaca terhadap
                orang-orang seperti Iki, Kelvin, Aldy, serta mereka yang sedang berjuang
                memadamkan api karhutla di luar sana. Mereka adalah korban atau orang
                terdampak yang bergerak akibat kebijakan sembrono pemerintah yang hanya
                mementingkan elit demi keuntungan fana.
            </p>
            <p>
                Harapan mereka dan masyarakat lainnya sama, hentikan dan berikan solusi
                nyata terhadap isu kebakaran hutan dan lahan. Jelas tidak akan ada
                keberlanjutan hidup di bumi selama krisis iklim dan kebakaran hutan masih
                menghantui. 
            </p>
            <p>
                “Kami berharap agar tak ada kebakaran hutan karena kepentingan orang kaya
                aja kak. Cukup sudah.” Pungkas Aldy tersedu. 
            </p>
            <p>
                “Kami punya keluarga, anak saya juga masih kecil. Mereka berhak hirup udara
                bersih. Banyak makhluk yang terkorbankan hanya karena kepentingan
                segelintir orang, itu parah.” Tambah Iki. 
            </p>

            <p>
                Perlu diingat, hak untuk bebas dari siksaan asap dan hak untuk hidup dengan
                layak merupakan hak fundamental bagi setiap manusia. Negara harus hadir dan
                mampu memastikan hak mendasar warga negara ini terpenuhi, bukan justru
                membuat kebijakan yang memicu api terus datang lagi dengan dalih investasi.
                Tak akan ada ekonomi di atas bumi yang mati. 
            </p>
