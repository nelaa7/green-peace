                2022 di Pontianak sebelum Tim Cegah Api bertugas di lapangan selama 30 hari. Mereka bersama-sama
                mengasah kemampuan dalam hal pemadaman kebakaran hutan agar
                semakin tangguh dan tanggap dalam beraksi mencegah api yang melahap hutan Indonesia.</p>
