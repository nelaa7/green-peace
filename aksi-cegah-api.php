            <h1 class="mb-4">Pelatihan Tim Cegah Api </h1>
            <p>Sebelum melakukan kegiatan pencegahan dan pemadaman api, Tim Cegah Api selalu dibekali dengan pelatihan
                khusus yang dibutuhkan untuk bertugas di lapangan. Tahun ini,
                Tim Cegah Api kembali dibekali dengan pelatihan seperti pemetaan, monitoring titik api, penggunaan
                peralatan pemadam kebakaran hutan, investigasi potensi kebakaran,
                edukasi pencegahan kebakaran, hingga pelatihan diskusi dan negosiasi dengan perusahaan dan pemerintah.
