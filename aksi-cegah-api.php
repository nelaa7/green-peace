            <p>Tim Cegah Api kembali untuk terjun langsung menangani kebakaran hutan yang tidak
                berkesudahan. Selain mengancam kehidupan manusia melalui polusi asapnya yang
                berdampak pada kesehatan kita, karhutla juga mengancam keberlangsungan hidup satwa
                liar asli Indonesia yang terancam punah.</p>
            <h1 class="mb-4">Selamatkan Hutan Indonesia dari Kebakaran Berkepanjangan</h1>
            <p>Tahun 2016, Tim Cegah Api dibentuk sebagai wujud dukungan Greenpeace untuk membebaskan hutan Indonesia
                dari situasi krisis kebakaran hutan dan lahan yang selalu
                terjadi setiap tahun. Puncaknya pada 2015 silam yang merupakan bencana asap terburuk yang dialami oleh
                Indonesia dikarenakan peningkatan yang masif akibat karhutla
                (bnpb.go.id).</p>
            <p>Terdiri dari relawan berbagai daerah, Tim Cegah Api berjuang 100% untuk hutan Indonesia. Mereka bekerja
                sama dengan masyarakat di areal terdampak untuk melakukan
                pemetaan, pemantauan titik api, dan mengawasi bila ada pengeringan kanal gambut baru, mengenalkan teknik
                pencegahan kebakaran di pemukiman, sekolah, lingkungan
                konsesi, juga institusi pemerintahan, dan banyak kegiatan lainnya.</p>
            <p>Tahun ini, setelah 2 tahun vakum akibat Pandemi COVID-19, Tim Cegah Api kembali untuk menyelamatkan hutan
                Indonesia tepatnya di Kubu Raya, Kalimantan Barat. Terdiri
                dari 19 relawan yang berasal dari Riau, Kalimantan, dan Jawa Timur, Tim Cegah Api mengawali kegiatan
                dengan pelatihan teknis dan non teknis sebelum bertugas langsung ke
                lapangan.</p>
